package com.shoook.entity;

public class UserLoginResponse {
	private int id;
	private int role;
	
	public UserLoginResponse() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getRole() {
		return role;
	}

	public void setRole(int role) {
		this.role = role;
	}
}
